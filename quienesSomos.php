        <strong>Seguridad:</strong> Es el conjunto de elementos estructurales, procesos, instrumentos y metodologías basadas en evidencias científicamente probadas que propenden por minimizar el riesgo de sufrir un evento adverso en el proceso de atención de salud o de mitigar sus consecuencias.
        
        <strong>Pertinencia:</strong> Es el grado en el cual los usuarios obtienen los servicios que requieren, con la mejor utilización de los recursos de acuerdo con la evidencia científica y sus efectos secundarios son menores que los beneficios potenciales.
        
        <strong>Continuidad:</strong> Es el grado en el cual los usuarios reciben las intervenciones requeridas, mediante una secuencia lógica y racional de actividades, basada en el conocimiento científico
        
