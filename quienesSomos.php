        <strong>ENFOQUE Y ORIENTACIÓN DE LAS ACTIVIDADES EN SALUD</strong>
        Tal como lo define el Decreto 1011 de 2006, que reglamenta el Sistema Obligatorio de Garantía de la Calidad en la atención, entendemos la calidad como la provisión de servicios de salud a los usuarios individuales y colectivos de manera accesible y equitativa, a través de un nivel profesional óptimo, teniendo en cuenta el balance entre beneficios, riesgos y costos, con el propósito de lograr la adhesión y satisfacción de dichos usuarios.

