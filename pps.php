            <p>
              La POLITICA DE PARTICIPACIÓN SOCIAL EN SALUD (PPSS) Resolución 2063 de 2017 es un esfuerzo del Ministerio de Salud y Protección Social para dar cumplimiento al mandato legal de direccionar y garantizar la participación social en el sector salud en el marco de la Ley 1438 de 2011, la Ley Estatutaria de Salud 1751 de 2015 y la Ley Estatuaria de Participación 1757 de 2015. busca dar respuestas a las problemáticas, necesidades, dificultades, oportunidades, limitaciones y debilidades que afectan la participación social en salud, en la perspectiva de dar cumplimiento al marco legal vigente y, por ende, a la realización del derecho humano de la participación que se encuentra vinculado bajo una lógica de interdependencia con el derecho a la salud.<br>
              Durante la vigencia 2021, la ESE Hospital san Vicente de Paul de Remedios, en articulación con la Secretaria de Salud de nuestro municipio, llevo a cabo el plan de acción proyectado para esta vigencia, en la cual se vieron beneficiados diferente actores grupos poblacionales y comunidad en general.  Con las capacitaciones brindadas y actividades desarrolladas durante esta vigencia, estamos seguros que nuestra comunidad y usuarios, cada día tendrán mas empoderamiento de los espacios de participación Social en Salud, que conlleven a representantes sociales con capacidad, habilidad y oportunidad para identificar problemáticas, necesidades, definir prioridades, formular y proponer ideas en la perspectiva del plan de salud.
