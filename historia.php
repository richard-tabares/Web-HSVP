              Esta institución es de carácter público y aunque no existe un escrito del surgimiento de la ESE, hasta la fecha, se conoce que el hospital san Vicente de Paúl, desde 1964 funcionó en una casona antigua de tapia, en el barrio el Retiro, lindando con el cementerio municipal.

              El hospital actual, fue creado mediante el Acuerdo Nro. 22, proferido por el Honorable Consejo Municipal el día 3 de diciembre de 1964 y reestructurado como Empresa Social del Estado por el Acuerdo Nro. 23 del 22 de mayo de 1994.

              Desde 1984 la ESE funciona en donde hoy está ubicada, en su momento disponía extensas zonas libres que le permitían grandes posibilidades de ampliación, en el mes de julio de 1991, por motivo de invasión de los terrenos adyacentes, el hospital perdió aproximadamente tres hectáreas de terreno en ladera debido a estos invasores, razón por el cual la Administración de esta época se vio obligada a construir una cerca perimetral en malla, la que actualmente existe. En este año el hospital tuvo una gran pérdida económica por lo que nunca los invasores aceptaron que este terreno era del hospital.

              En 1997 se realizó la primera construcción de ampliación la cual sería la solución para el poco espacio de hospitalización, se construyeron 4 habitaciones con espacio para dos camas, una sala de pediatría, pieza para depósito, almacén, mantenimiento, garaje y tienda.

              La ESE, se construyó cumpliendo con los requisitos del Ministerio de Salud, para que funcionara como hospital de primer nivel, lo cual se viene cumpliendo en la actualidad.

